    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        String url = "http://192.168.20.172/api/createTemporaryUser/?";
        String name = jTextField1.getText();
        String children = jTextField2.getText();
        
        String data = "name=" +name + "&children=" + children;
        
		 if ((!"".equals(name))&(!"".equals(children))){
		            
		        	SendRequest request = new SendRequest("POST", url, data);
		        	request.execute();
		        	
		        	if(request.getResponseCode() == -100){
		        	
		        		JOptionPane.showConfirmDialog(null, "Проверьте подключение к сети", "Ошибка", JOptionPane.CLOSED_OPTION);
		        	}
		            
		            
		            
		            if(request.getResponseCode() == -50){
		                JOptionPane.showConfirmDialog(null, "Проверьте введенные данные", "Неверный логин или пароль", JOptionPane.CLOSED_OPTION);
		            }
		            
		            if(request.getResponseCode() == 200){           
		                try {
		                    JSONParser parser = new JSONParser();
		                    JSONObject jsonObject;
		                    jsonObject = (JSONObject) parser.parse(request.getResponse());
		                    String id = (String) jsonObject.get("id");
		                    
		                    global.setId(id);
		                    this.setVisible(false);
		                    new CreateForm(global).setVisible(true);
		                } catch (ParseException ex) {
		                    Logger.getLogger(LoginForm.class.getName()).log(Level.SEVERE, null, ex);
		                }
		                
		            } 
		        }
		        
        
        
        
        
    }

